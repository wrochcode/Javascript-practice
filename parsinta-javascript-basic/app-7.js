//switch case
const age = 19;
switch (age) {
    case 20:
        console.log("Yes i'm 20");
        break;
    case 21:
        console.log("Yes i'm 21");
        break;
    case 22:
        console.log("Yes i'm 22");
        break;
    case 23:
        console.log("Yes i'm 23");
        break;
    default:
        console.log("Undefined");
}